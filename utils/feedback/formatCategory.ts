export function formatCategory(categoryId: string) {
  switch (categoryId) {
    case "ui":
      return "UI"
    case "ux":
      return "UX"
    case "feature":
      return "Feature"
    case "enhancement":
      return "Enhancement"
    case "feature":
      return "Feature"
    case "bug":
      return "Bug"
  }
}
